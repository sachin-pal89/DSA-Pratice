//Program to Depict the size of various data types
#include<bits/stdc++.h>
using namespace std;

int main()
{
    cout<<"\nSize of int: "<<sizeof(int)<<"\n";
    cout<<"\nSize of char: "<<sizeof(char)<<"\n";
    cout<<"\nSize of bool: "<<sizeof(bool)<<"\n";
    cout<<"\nSize of float: "<<sizeof(float)<<"\n";
    cout<<"\nSize of double: "<<sizeof(double)<<"\n";
    cout<<"\nSize of long int: "<<sizeof(long int)<<"\n";
    cout<<"\nSize of short int: "<<sizeof(short int)<<"\n";
    cout<<"\nSize of long long int: "<<sizeof(long long int)<<"\n";
    cout<<"\nSize of long double : "<<sizeof(long double)<<"\n";
    return 0;
}